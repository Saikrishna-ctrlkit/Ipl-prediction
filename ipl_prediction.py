# STEP 2: FEATURE ENGINEERING

# Sort by match, innings, over, ball
df = df.sort_values(['match_id', 'innings', 'over', 'ball']).reset_index(drop=True)

# Current Run Rate
df['current_run_rate'] = np.where(
    df['team_balls'] > 0,
    df['team_runs'] / (df['team_balls'] / 6),
    0
)

# Balls remaining
df['balls_remaining'] = (120 - df['team_balls']).clip(lower=0)
df['overs_remaining'] = df['balls_remaining'] / 6

# Runs needed (2nd innings)
df['runs_needed'] = df['runs_target'] - df['team_runs']

# Required Run Rate
df['required_run_rate'] = np.where(
    (df['innings'] == 2) & (df['overs_remaining'] > 0),
    df['runs_needed'] / df['overs_remaining'],
    0
).clip(0)

# Pressure Index
df['pressure_index'] = np.where(
    df['current_run_rate'] > 0,
    df['required_run_rate'] / df['current_run_rate'],
    0
)

# Extra features
df['wickets_remaining'] = 10 - df['team_wicket']
df['is_powerplay']      = (df['over'] <= 6).astype(int)
df['is_death_over']     = (df['over'] >= 16).astype(int)
