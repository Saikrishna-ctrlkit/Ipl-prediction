def safe_encode(le, val):
    val = str(val)
    return list(le.classes_).index(val) if val in le.classes_ else -1

def predict_score(batting_team, bowling_team, batter, bowler,
                  venue, city, team_runs, team_balls, team_wicket,
                  bat_pos, batter_runs, batter_balls, over, ball,
                  toss_winner, toss_decision, month=4, day=15):

    crr = team_runs / (team_balls/6) if team_balls > 0 else 0
    balls_remaining = max(0, 120 - team_balls)

    row = pd.DataFrame([{
        'team_runs': team_runs, 'team_balls': team_balls,
        'team_wicket': team_wicket, 'wickets_remaining': 10 - team_wicket,
        'current_run_rate': crr, 'balls_remaining': balls_remaining,
        'over': over, 'ball': ball, 'bat_pos': bat_pos,
        'batter_runs': batter_runs, 'batter_balls': batter_balls,
        'is_powerplay': int(over<=6), 'is_death_over': int(over>=16),
        'batting_won_toss': int(batting_team==toss_winner),
        'toss_bat': int(toss_decision=='bat'),
        'batting_team_enc': safe_encode(le_dict['batting_team'], batting_team),
        'bowling_team_enc': safe_encode(le_dict['bowling_team'], bowling_team),
        'batter_enc': safe_encode(le_dict['batter'], batter),
        'bowler_enc': safe_encode(le_dict['bowler'], bowler),
        'venue_enc': safe_encode(le_dict['venue'], venue),
        'city_enc': safe_encode(le_dict['city'], city),
        'month': month, 'day': day
    }])

    pred = score_model.predict(row[score_features])[0]
    print(f"\n{'='*40}")
    print(f"  {batting_team} vs {bowling_team}")
    print(f"  Score: {team_runs}/{team_wicket} in over {over}.{ball}")
    print(f"  ✅ Predicted Final Score: {int(pred)} runs")
    print(f"{'='*40}")


def predict_winner(batting_team, bowling_team, batter, bowler,
                   venue, city, team_runs, team_balls, team_wicket,
                   bat_pos, batter_runs, batter_balls, over, ball,
                   runs_target, toss_winner, toss_decision, month=4, day=15):

    crr = team_runs / (team_balls/6) if team_balls > 0 else 0
    balls_remaining = max(0, 120 - team_balls)
    overs_remaining = balls_remaining / 6
    runs_needed = runs_target - team_runs
    rrr = runs_needed / overs_remaining if overs_remaining > 0 else 0
    pressure = rrr / crr if crr > 0 else 0

    row = pd.DataFrame([{
        'team_runs': team_runs, 'team_balls': team_balls,
        'team_wicket': team_wicket, 'wickets_remaining': 10 - team_wicket,
        'current_run_rate': crr, 'required_run_rate': max(0,rrr),
        'balls_remaining': balls_remaining, 'runs_target': runs_target,
        'runs_needed': runs_needed, 'pressure_index': pressure,
        'over': over, 'ball': ball, 'bat_pos': bat_pos,
        'batter_runs': batter_runs, 'batter_balls': batter_balls,
        'is_powerplay': int(over<=6), 'is_death_over': int(over>=16),
        'batting_won_toss': int(batting_team==toss_winner),
        'toss_bat': int(toss_decision=='bat'),
        'batting_team_enc': safe_encode(le_dict['batting_team'], batting_team),
        'bowling_team_enc': safe_encode(le_dict['bowling_team'], bowling_team),
        'batter_enc': safe_encode(le_dict['batter'], batter),
        'bowler_enc': safe_encode(le_dict['bowler'], bowler),
        'venue_enc': safe_encode(le_dict['venue'], venue),
        'city_enc': safe_encode(le_dict['city'], city),
        'month': month, 'day': day
    }])

    proba = match_model.predict_proba(row[match_features])[0]
    print(f"\n{'='*40}")
    print(f"  {batting_team} vs {bowling_team}")
    print(f"  Chasing {runs_target} | {team_runs}/{team_wicket} in over {over}.{ball}")
    print(f"  Need {int(runs_needed)} off {balls_remaining} balls")
    print(f"  CRR: {crr:.2f} | RRR: {rrr:.2f}")
    print(f"  🏏 {batting_team}: {proba[1]*100:.1f}%")
    print(f"  🎳 {bowling_team}: {proba[0]*100:.1f}%")
    print(f"{'='*40}")


